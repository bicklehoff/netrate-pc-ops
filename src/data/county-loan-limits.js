/**
 * Normalize county name for matching (case-insensitive, strips common suffixes)
 */
function normalizeCounty(county) {
  return county
    .trim()
    .toLowerCase()
    .replace(/\s+county$/i, '')
    .replace(/\s+parish$/i, '')
    .replace(/\s+borough$/i, '')
    .replace(/\s+census area$/i, '')
    .replace(/\s+municipio$/i, '')
    .replace(/\s+municipality$/i, '');
}

